/*

go语言中 现存的关键字 25个

break ：中断本层循环，直接退出本层循环
case ：一般与switch一起做条件选择，还可以与select结合使用
chan ：信道定义
const ：常量声明
continue：中断本次循环，进入下一次循环
default：与switch一起使用设置默认值
func ：定义方法(函数)的关键字
defer ：方法最后执行的操作，新定义的后执行
go：开启go-routine
else:与if以前做条件筛选
goto：转向指定代码模块
fallthrough：与switch一起继续执行下面的case条件
if：条件判断
for：循环语句关键字
import：导入依赖包
interface ：接口类型
map ：map集合类型 key-value存储
package：包名定义关键字 package+包名
range ：遍历数据的关键字
return：方法返回、程序退出
select：用来监听和channel有关的IO操作
struct：定义实例模型的关键字,与type一起使用
switch ：做条件选择，与if else功能类型
type ：定义模型的关键字
var：定义变量的关键字
 */
